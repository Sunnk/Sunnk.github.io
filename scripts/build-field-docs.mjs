import { readFileSync, readdirSync, statSync, writeFileSync } from 'node:fs'
import { join, relative, resolve } from 'node:path'
import { marked } from 'marked'

const FIELDS_DIR = resolve('.vitepress/components/tlpData/fields')
const OUT_FILE = resolve('.vitepress/components/tlpData/fieldDocs.generated.ts')

function parseFrontmatter(src) {
  const match = src.match(/^---\r?\n([\s\S]*?)\r?\n---\r?\n?/)
  if (!match) return { meta: {}, body: src }
  const meta = {}
  for (const line of match[1].split('\n')) {
    const idx = line.indexOf(':')
    if (idx === -1) continue
    meta[line.slice(0, idx).trim()] = line.slice(idx + 1).trim()
  }
  return { meta, body: src.slice(match[0].length) }
}

function build() {
  if (!statSync(FIELDS_DIR, { throwIfNoEntry: false })) {
    console.log('[build-field-docs] fields/ directory not found, skipping.')
    writeFileSync(OUT_FILE, '// Auto-generated — no fields found\nexport const fieldDocs: Record<string, { description: string; html: string }> = {}\n')
    return
  }

  const entries = readdirSync(FIELDS_DIR).filter(name => {
    const p = join(FIELDS_DIR, name, 'index.md')
    return statSync(p, { throwIfNoEntry: false })?.isFile()
  })

  const docs = {}

  for (const name of entries) {
    const mdPath = join(FIELDS_DIR, name, 'index.md')
    const raw = readFileSync(mdPath, 'utf-8')
    const { meta, body } = parseFrontmatter(raw)
    const html = marked(body, { breaks: false, gfm: true })
    docs[name] = {
      description: meta.description || '',
      html,
    }
    console.log(`[build-field-docs] ${name} — ${html.length} chars`)
  }

  const ts = `// Auto-generated by scripts/build-field-docs.mjs — DO NOT EDIT
export const fieldDocs: Record<string, { description: string; html: string }> = ${JSON.stringify(docs, null, 2)}
`
  writeFileSync(OUT_FILE, ts)
  console.log(`[build-field-docs] Wrote ${Object.keys(docs).length} entries to ${relative(process.cwd(), OUT_FILE)}`)
}

build()
